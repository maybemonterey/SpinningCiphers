<h2 class="cipher_title" style="text-align: left; padding: 10px;">What is the Famed Enigma?</h2>
<table><tr><td><img src="https://upload.wikimedia.org/wikipedia/commons/b/bd/Enigma_%28crittografia%29_-_Museo_scienza_e_tecnologia_Milano.jpg" alt="The Enigma Machine" width="377.66" height="346.33" style="margin-right: 10px"></td><td>
                <p class="p" style="text-align: justify;">The Enigma machine was a field unit used in World War II by German field agents to encrypt and
                decrypt
                messages and communications. It employed a series of rotors that, with some electricity, a light bulb, and a
                reflector, allowed the operator to either encrypt or decrypt a message.</p>

                <p class="p" style="text-align: justify">The original position of the rotors, set with each encryption and based on a prearranged pattern
                that in turn was based on the calendar, allowed the machine to be used even if it was compromised. When the
                Enigma
                was in use, with each subsequent key press, the rotors would change in alignment from their set positions in
                such a
                way that a different letter was produced each time. With a message in hand, the operator would enter each
                character
                into the machine by pressing a typewriter-like key. The rotors would align and a letter would then illuminate,
                telling the operator what the letter really was. Likewise, when enciphering, the operator would press the key
                and
                the illuminated letter would be the cipher text. The continually changing internal flow of electricity that
                caused
                the rotors to change was not random, but it created a polyalphabetic cipher that could be different each time it
                was
                used.</p>
