    <h2 class="cipher_title" style="text-align: left; padding: 10px;">What is a Cipher?</h2>

    <p class="p" style="text-align: justify;">A cipher is any method of transforming a message to conceal its meaning. The term is also used
        synonymously
        with ciphertext or cryptogram in reference to the encrypted form of the message.</p>

    <p class="p" style="text-align: justify;">All ciphers involve either transposition or substitution, or a combination of these two mathematical
        operations—i.e., product ciphers. In transposition cipher systems, elements of the plaintext (e.g., a letter,
        word,
        or string of symbols) are rearranged without any change in the identity of the elements. In substitution
        systems,
        such elements are replaced by other objects or groups of objects without a change in their sequence. In systems
        involving product ciphers, transposition and substitution are cascaded; for example, in a system of this type
        called
        a fractionation system, a substitution is first made from symbols in the plaintext to multiple symbols in the
        ciphertext, which is then superencrypted by a transposition. All operations or steps involved in the
        transformation
        of a message are carried out in accordance to a rule defined by a secret key known only to the sender of the
        message
        and the intended receiver.</p>
</div>
<div>
    <h2 class="cipher_title" style="text-align: left; padding: 10px;">Some important dated Ciphers</h2>
    <p class="p" style="text-align: justify;">Cipher devices or machines have commonly been used to encipher and decipher messages. The first cipher
        device appears to have been employed by the ancient Greeks around 400 BCE for secret communications between
        military commanders. This device, called the scytale, consisted of a tapered baton around which was spirally
        wrapped a piece of parchment inscribed with the message. When unwrapped the parchment bore an incomprehensible
        set of letters, but when wrapped around another baton of identical proportions, the original text reappeared.
        Other simple devices known as cipher disks were used by European governments for diplomatic communications by
        the late 1400s. These devices consisted of two rotating concentric circles, both bearing a sequence of 26
        letters. One disk was used to select plaintext letters, while the other was used for the corresponding cipher
        component.</p>

    <p class="p" style="text-align: justify;">In 1891 Étienne Bazeries, a French cryptologist, invented a more sophisticated cipher device based on
        principles formulated by Thomas Jefferson of the United States nearly a century earlier. Bazeries’s so-called
        cylindrical cryptograph was made up of 20 numbered rotatable disks, each with a different alphabet engraved on
        its periphery. The disks were arranged in an agreed-upon order on a central shaft and rotated so that the first
        20 letters of the message plaintext appeared in a row; the ciphertext was then formed by arbitrarily taking off
        any other row. The remaining letters of the message were treated in the same way, 20 letters at a time.</p>

    <p class="p" style="text-align: justify;">Advances in radio communications and electromechanical technology in the 1920s brought about a
        revolution in cryptodevices—the development of the rotor cipher machine. One common type of rotor system
        implemented product ciphers with simple monoalphabetic substitution ciphers as factors. The rotors in this
        machine consisted of disks with electrical contacts on each side that were hardwired to realize an arbitrary set
        of one-to-one connections (monoalphabetic substitution) between the contacts on opposite sides of the rotor.</p>

    <p class="p" style="text-align: justify;">The rotor cipher machine was used extensively by both the Allied and the Axis powers during World War
        II, with the most notable such device being the German Enigma machine. The application of electronic components
        in subsequent years resulted in significant increases in operation speed though no major changes in basic
        design. Since the early 1970s, cryptologists have adapted major developments in microcircuitry and computer
        technology to create new, highly sophisticated forms of cryptodevices and cryptosystems, as exemplified by the
        Fibonacci generator and the implementation of the Data Encryption Standard (DES) through the use of
        microprocessors.</p>

